    private void jBAdicionarCPFProfessorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAdicionarCPFProfessorActionPerformed
        
        String CPFProfessor = jTAdicionarCPFProfessor.getText();
        Boolean Find = false;
        
        for (Professores elementoProfessores: Professores.ListaProfessores) {
            if(CPFProfessor.equals(elementoProfessores.getCPF())){
                tableModel.addRow(new Object[]{elementoProfessores.getNomeProfessor(),elementoProfessores.getCPF(),elementoProfessores.getDisciplinaProfessor().toString().replace("Materia: ", "")});
                Find=true;
            }
        }
        
        if(!Find){
            JOptionPane.showMessageDialog(null, "Houve um erro ao adicionar o professor a turma. Professor nao encontrado no sistema", "Cadastro de Turmas", JOptionPane.ERROR_MESSAGE);
        }
        
        jTAdicionarCPFProfessor.setText("");
        
    }//GEN-LAST:event_jBAdicionarCPFProfessorActionPerformed

