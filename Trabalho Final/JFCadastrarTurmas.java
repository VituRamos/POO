    private void jBAdicionarRAAlunoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAdicionarRAAlunoActionPerformed
        
        String RAAluno = jTAdicionarRAAluno.getText();
        Boolean Find = false;
        
        for (Alunos elementoAlunos: Alunos.ListaAlunos) {
            if(RAAluno.equals(elementoAlunos.getRA())){
                tableModel2.addRow(new Object[]{elementoAlunos.getNomeAluno(),elementoAlunos.getRA()});
                Find = true;
            }
        }
        
        if(!Find){
            JOptionPane.showMessageDialog(null, "Houve um erro ao adicionar o aluno a turma. Aluno nao encontrado no sistema", "Cadastro de Turmas", JOptionPane.ERROR_MESSAGE);
        }
        
        jTAdicionarRAAluno.setText("");
        
    }//GEN-LAST:event_jBAdicionarRAAlunoActionPerformed

    private void jBCadastrarTurmaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBCadastrarTurmaActionPerformed
        
        String NomeTurma = jTCadastrarNomeTurma.getText();
        
        int totalRowsProfessores = jTableProfessores.getRowCount();
        ArrayList<Professores> professoresTurma = new ArrayList<>();
        int totalRowsAlunos = jTableAlunos.getRowCount();
        ArrayList<Alunos> AlunosTurma = new ArrayList<>();
        
        for (int linha = 0; linha < totalRowsProfessores; linha++) {
            String CPFProfessor = jTableProfessores.getValueAt(linha, 1).toString();
            professoresTurma.add(gerenciarprofessores.consultarProfessores(CPFProfessor));
        }
        
        for (int linha = 0; linha < totalRowsAlunos; linha++) {
            String RAAlunos = jTableAlunos.getValueAt(linha, 1).toString();
            AlunosTurma.add(gerenciaralunos.consultarAlunos(RAAlunos));
        }
        
        gerenciarturmas.cadastrarTurma(NomeTurma, professoresTurma, AlunosTurma);
        limpaTabelaProfessores();
        limpaTabelaAlunos();
        jTCadastrarNomeTurma.setText("");
        jTAdicionarCPFProfessor.setText("");
        jTAdicionarRAAluno.setText("");
       
        
    }//GEN-LAST:event_jBCadastrarTurmaActionPerformed

